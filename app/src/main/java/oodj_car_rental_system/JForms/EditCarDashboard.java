private Car getCarInformation() throws  Exception{
        String name = getCarName();
        float pricePerHour = getPricePerHour();
        int seats = getCarSeats();
        CarType carType = getCarType();
        CarFuel carFuel = getCarFuel();
        GearBoxType gearBoxType = getGearBoxType();

        return new Car(name, pricePerHour, carType, carFuel, seats, gearBoxType, true);
    }

    private  String getCarName() throws Exception{
        String name = name_tf.getText();
        if (name.equals("")){
            throw new Exception("Name field is required");
        }
        return  name;
    }
    private int getCarSeats() throws Exception{
        int seats;
        try {

            seats = (int) seats_sp.getValue();
        } catch (Exception e){
            throw new Exception("Seats should be an numeric value");
        }
        if (seats <= 0){
            throw new Exception("Seats should be an more than 0");
        }

        return seats;
    }


    private GearBoxType getGearBoxType() throws Exception{
       if (gearbox_chb.getSelectedItem() == null) {
           throw new Exception("Invalid Car Fuel Type");
       }
       String gearBoxVal = gearbox_chb.getSelectedItem().toString();
       return CarSerializer.deserializeGearBox(gearBoxVal).orElseThrow();
    }

    private CarFuel getCarFuel() throws  Exception{
       if (car_fuel_chb.getSelectedItem() == null){
           throw new Exception("Invalid Car Fuel Type");
       }
       String carFuelVal = car_fuel_chb.getSelectedItem().toString();
       return CarSerializer.deserializeCarFuel(carFuelVal).orElseThrow();
    }

    private CarType getCarType() throws Exception{
        if (car_type_chb.getSelectedItem() == null){
           throw new Exception("Invalid Car Type");
        }
        String carTypeVal = car_type_chb.getSelectedItem().toString();
        return CarSerializer.deserializeCarType(carTypeVal).orElseThrow();

    }
    private float getPricePerHour() throws Exception{
        float price_per_hour = 0;
        try{
            price_per_hour = Float.valueOf(price_per_hour_tf.getText());
        } catch (Exception e){
            throw new Exception("Invalid Price Value");
        }

        if (price_per_hour <= 0){
            throw new Exception("Price should be more than 0");
        }

        return price_per_hour;
    }

