        private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
            if (this.car == null){
                JOptionPane.showMessageDialog(null, "Something went Wrong");
                return;
            }

            String carTypeVal =CarSerializer.serializeCarType(this.car.getCarType()).orElseThrow();
            String carFuelVal = CarSerializer.serializeCarFuel(this.car.getCarFuel()).orElseThrow();
            String gearBoxVal =  CarSerializer.serializeGearBoxType(this.car.getGearBoxType()).orElseThrow();
            this.name_tf.setText(car.getName());
            this.price_per_hour_tf.setText(String.valueOf(this.car.getPricePerHour()));
            this.car_type_chb.getModel().setSelectedItem(carTypeVal);
            this.car_fuel_chb.getModel().setSelectedItem(carFuelVal);
            this.gearbox_chb.getModel().setSelectedItem(gearBoxVal);
            this.seats_sp.setValue(this.car.getSeats());
        }//GEN-LAST:event_formWindowOpened

